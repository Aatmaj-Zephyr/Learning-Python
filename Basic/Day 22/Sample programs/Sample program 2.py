#1. Upper case the first letter in this sentence:
txt = "hello, AND Welcome"
x = txt.capitalize()
print(x)
#output : Hello, and welcome

#2. Make the string lower case:
txt = "Hello, And Welcome!@#$"
x = txt.lower()
print(x)
#output : hello, and welcome

#3. Make the string upper case:
txt = "Hello, And Welcome"
x = txt.upper()
print(x)
#output : HELLO, AND WELCOME
